package ss1_Introduction_to_Java.practice;

public class DeclaringAndUsingVariables {

    //[Thực hành] Khai báo và sử dụng biến

    public static void main(String[] args) {
        int i = 1; //Khai báo biến i là kiểu int có giá trị là 1
        float f = 2.5f; // Khai báo biến f là kiểu float có giá trị là 2.5
        double d = 3.5; // Khai báo biến d là kiểu double có giá trị là 3.5
        boolean b = true; // Khai báo biến b là kiểu boolean có giá trị là true
        char c = 'a'; // Khai báo biến c là kiểu char có giá trị là ký tự a
        String s = "Hà Nội"; // Khai báo biến s có kiểu
        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(c);
        System.out.println(s);
    }
}

